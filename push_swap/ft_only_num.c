int	ft_only_num(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '\0')
		return (0);
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] < 58 && str[i] > 47)
		i++;
	if (str[i] != '\0')
		return (0);
	return (1);
}